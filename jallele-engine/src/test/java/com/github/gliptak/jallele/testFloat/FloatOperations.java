package com.github.gliptak.jallele.testFloat;

public class FloatOperations {
	public float addTwo(float f){
		return 2.0f + f;
	}
	
	public float multiplyBy(float f, float multiplier){
		return f * multiplier;
	}
	
	public float divideBy(float f, float divisor){
		return f / divisor;
	}
	
	public float subtractFrom(float f, float subtrahend){
		return f - subtrahend;
	}
	
	public float remainder(float f, float divisor){
		return f % divisor;
	}
}